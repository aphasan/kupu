package id.my.mdn.kupu.core.reporting.exception;

/**
 *
 * @author aphasan
 */
public class ReportFillingException extends Exception {

    public ReportFillingException() {
    }

    public ReportFillingException(String message) {
        super(message);
    }

    public ReportFillingException(String message, Throwable cause) {
        super(message, cause);
    }

    public ReportFillingException(Throwable cause) {
        super(cause);
    }

    public ReportFillingException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
